# 리스트가 "번호표(0, 1, 2...)"가 붙은 사물함이라면,

# 딕셔너리는 "이름표(Key)"가 붙은 사물함입니다.

# 2. 언제 쓰나요?

# my_list[3] 처럼 숫자로 데이터를 찾는 게 헷갈릴 때 씁니다.

# "이름"을 검색하면 "전화번호"가 나오고, "Apple"을 검색하면 "사과"가 나오는 사전(Dictionary) 같은 구조가 필요할 때 사용합니다.

# 3. 구조: Key(열쇠) : Value(값)

# 반드시 중괄호 { } 를 사용합니다.

# Key: 값을 찾기 위한 유일한 이름표 (중복 불가)

# Value: 실제 들어있는 데이터 (무엇이든 가능)

# 비유:

# 리스트: 학교 출석부 (1번, 2번, 3번... 순서가 중요함)

# 딕셔너리: 스마트폰 주소록 ("철수"를 검색하면 번호가 나옴, 순서는 중요하지 않음)

# ==========================================
# [실습] 딕셔너리 기초 (게임 캐릭터 만들기)
# ==========================================

# 1. 딕셔너리 만들기 { Key : Value }
# Key는 주로 문자열(" ")을 사용합니다.
my_hero = {
    "이름": "전설의 용사",
    "레벨": 1,
    "HP": 100,
    "스킬": ["베기", "방어"] # 리스트도 값으로 넣을 수 있어요!
}

print(f"초기 상태: {my_hero}")
print("-" * 30)

# 2. 데이터 꺼내기 (조회)
# 리스트는 [0]을 쓰지만, 딕셔너리는 ["Key"]를 씁니다.
print(f"캐릭터 이름: {my_hero['이름']}")
print(f"현재 체력: {my_hero['HP']}")


# 3. 데이터 수정하기 (레벨 업!)
# 변수처럼 값을 대입하면 바뀝니다.
my_hero["레벨"] = 2
print(f"🎉 레벨이 {my_hero['레벨']}로 올랐습니다!")


# 4. 데이터 추가하기 (새로운 아이템 획득)
# 없던 Key에 값을 넣으면 자동으로 추가됩니다. (아주 편리함)
my_hero["무기"] = "엑스칼리버"
print(f"득템! [{my_hero['무기']}]을(를) 장착했습니다.")


# 5. 데이터 삭제하기 (HP 삭제??)
del my_hero["HP"]

print("-" * 30)
print(f"최종 상태: {my_hero}")

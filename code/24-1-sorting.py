# ==========================================
# 버블 정렬 (Bubble Sort) 체험하기
# ==========================================

# 정렬되지 않은 숫자들
nums = [5, 2, 9, 1, 6]
print(f"정렬 전: {nums}")
print("-" * 30)

n = len(nums)

# 1. 버블 정렬 알고리즘 (원리 이해용)
# 모든 숫자를 하나씩 비교하며 자리를 찾아갑니다.
for i in range(n):
    # 뒤에서부터 i개는 이미 정렬이 끝났으므로 제외하고 반복
    for j in range(0, n - i - 1):
        
        # 앞사람(j)이 뒷사람(j+1)보다 크다면?
        if nums[j] > nums[j + 1]:
            # 자리 바꾸기 (Swap)
            # 파이썬은 임시 변수 없이 이렇게 한 방에 바꿀 수 있습니다!
            nums[j], nums[j + 1] = nums[j + 1], nums[j]
            
            # 과정 보여주기
            print(f"자리 바꿈! {nums[j]} <-> {nums[j+1]} : {nums}")

print("-" * 30)
print(f"직접 정렬한 결과: {nums}")

# 2. 파이썬 내장 함수 
# 사실 개발자들은 sort() 함수를 씁니다. (훨씬 빠르고 간편함)
nums2 = [100, 50, 30, 200, 10]
nums2.sort() 
print(f"\nsort() 함수 사용: {nums2}")

# while 반복문 기본 예제 (주석을 읽고 실행해보세요)

# 기본 구조:
# while 조건:
#     실행문
# 조건이 False가 되면 반복 종료

# 예: 조건이 3보다 작을 동안 반복
i = 0
print("while 기본 예제: i가 3보다 작을 동안 반복")
while i < 3:
    print("현재 i =", i)
    i = i + 1  # i를 증가시키지 않으면 무한루프에 빠집니다

print("반복 종료. 최종 i =", i)


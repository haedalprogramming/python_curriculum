# 선형 탐색이란?

# Linear(선형): 선처럼 일자로 쭉 뻗어있다는 뜻입니다.

# 데이터가 정렬되어 있지 않을 때 (뒤죽박죽일 때) 사용할 수 있는 유일한 방법입니다.

# 0번 칸부터 마지막 칸까지 순서대로 하나씩 확인합니다.

# 장점: 구현이 엄청 쉽습니다. 정렬 안 해도 됩니다.

# 단점: 운이 좋으면 한 번에 찾지만, 운이 나쁘면(찾는 게 맨 뒤에 있거나 없으면) 끝까지 다 봐야 해서 느립니다.


# ==========================================
# 선형 탐색 (Linear Search) 원리
# ==========================================
import time

# 범인(찾는 숫자)이 숨어있는 아파트 (정렬 안 된 리스트)
apt = [10, 5, 33, 2, 7, 100, 42, 9]
target = 7  # 찾고 싶은 범인 번호

print(f"아파트 입주민: {apt}")
print(f"찾는 범인: {target}")
print("-" * 30)

# 탐색 시작 (처음부터 끝까지)
found = False # 찾았는지 표시하는 깃발 (Flag 변수)

for i in range(len(apt)):
    # 탐색 과정 시각화 (천천히 보여주기)
    print(f"🔎 {i}번 방 확인 중... (주민: {apt[i]})")
    time.sleep(0.5)
    
    # 범인을 찾았다!
    if apt[i] == target:
        print(f"🎉 잡았다! {target}은 {i}번 인덱스에 있습니다.")
        found = True
        break # 찾았으면 더 이상 고생할 필요 없음 (반복 종료)

# 끝까지 다 뒤졌는데 깃발이 안 올라갔다면?
if found == False:
    print("😭 범인을 찾지 못했습니다. (리스트에 없음)")
# 도서관 책 정리하기 (가나다순)
# ==========================================

# 1. 정리되지 않은 책 목록
books = ["파이썬", "HTML", "자바스크립트", "C언어", "알고리즘"]

print(f"📚 정리 전: {books}")
print("-" * 30)

# 2. 사서님(사용자)에게 새 책 입력받기
new_book = input("새로 들어온 책 이름을 입력하세요: ")

# 리스트에 추가 (맨 뒤에 붙음)
books.append(new_book)
print(f"\n[추가됨] 현재 상태: {books}")

# 3. 가나다순(ABC순)으로 정렬하기
# 문자열을 sort() 하면 한글(가나다) -> 영어(ABC) 순서로 정렬됩니다.
n = len(books) # 책의 총 개수

# 첫 번째 반복문: 총 n번만큼 반복 (전체 순회)
for i in range(n):
    # 두 번째 반복문: 정렬 안 된 부분끼리 비교
    # (이미 뒤로 보낸 i개는 비교할 필요 없으므로 n - i - 1)
    for j in range(0, n - i - 1):
        
        # 앞의 책 제목이 뒤의 책 제목보다 크다(사전 순서상 뒤)면?
        if books[j] > books[j + 1]:
            # 자리 바꾸기 (Swap)
            books[j], books[j + 1] = books[j + 1], books[j]
            
            # (선택) 정렬 과정을 눈으로 보고 싶다면 주석 해제하세요
            print(f"자리가 바뀝니다: {books}")

print("\n🧹 책장을 정리하는 중입니다...")
# books.sort() 파이썬이 편한이유

# 4. 결과 확인
print("-" * 30)
print(f"✨ 정리 후: {books}")


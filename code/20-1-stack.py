# 스택(Stack)이란?

# 데이터를 차곡차곡 쌓아 올리는 구조입니다.

# 비유 1: 프링글스 감자칩

# 공장에서 감자칩을 넣을 때, 바닥부터 차례로 넣습니다.

# 우리가 먹을 때는요? 맨 마지막에 넣은(맨 위에 있는) 감자칩을 가장 먼저 먹게 됩니다.

# 비유 2: 웹 브라우저 '뒤로 가기'

# 네이버 → 구글 → 유튜브 순서로 접속했습니다.

# '뒤로 가기'를 누르면? 가장 최근에 본 '유튜브'가 나오고, 그 다음 '구글'이 나옵니다.

# 이것을 LIFO (Last In, First Out), 우리말로는 후입선출(나중에 들어간 게 먼저 나온다)이라고 부릅니다.

# 파이썬 리스트로 스택 만들기

# 파이썬의 리스트는 이미 스택의 기능을 다 가지고 있습니다.

# append() = Push (밀어 넣기): 리스트의 맨 뒤(위)에 데이터를 추가합니다.

# pop() = Pop (꺼내기): 리스트의 맨 뒤(위) 데이터를 꺼내고 삭제합니다.
# ==========================================
# 텍스트 에디터 '실행 취소' 만들기
# ==========================================
# 스택(Stack)의 원리: LIFO (Last In, First Out)
# 파이썬 리스트의 append()가 Push, pop()이 Pop 역할을 합니다.

action_stack = []

print("📝 초간단 메모장 (명령어: 쓰기, 취소, 확인, 종료)")

while True:
    print("-" * 30)
    command = input("명령 입력 >> ")
    
    # 1. 쓰기 (Push): 스택에 데이터를 쌓음
    if command == "쓰기":
        word = input("적을 단어: ")
        action_stack.append(word)
        print(f"'{word}'을(를) 적었습니다.")
        
    # 2. 취소 (Pop): 스택의 맨 위(가장 최근) 데이터를 꺼냄
    elif command == "취소":
        # 주의! 스택이 비어있는데 끄집어내면 에러가 납니다.
        if len(action_stack) == 0:
            print("더 이상 취소할 작업이 없습니다! (스택 텅 빔)")
        else:
            deleted = action_stack.pop() # 맨 위 데이터 삭제
            print(f"실행 취소: '{deleted}' 삭제됨")
            
    # 3. 확인 (Peek): 현재 스택 상태 확인
    elif command == "확인":
        print(f"현재 메모장 상태: {action_stack}")
        
    elif command == "종료":
        break
    else:
        print("잘못된 명령어입니다.")


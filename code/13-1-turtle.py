# 거북이는 '로봇 화가'다

# 우리가 print()만 쓸 때는 검은 화면에 글씨만 나왔지만, turtle을 부르면 하얀 캔버스(종이)가 나타납니다.

# 화면 가운데에 있는 화살표(또는 거북이)가 바로 우리의 로봇입니다.

# 이 로봇은 배에 색연필을 달고 있어서, 기어가는 자리에 선이 쭉 그어집니다.

# 거북이가 사는 세상: 좌표계 (X, Y)

# 거북이 화면은 수학 시간에 본 **모눈종이(그래프)**와 똑같습니다.

# 0, 0 (원점): 화면의 정중앙. 거북이가 태어나는 곳입니다.

# X축: 가로. 오른쪽이 플러스(+), 왼쪽이 마이너스(-)입니다.

# Y축: 세로. 위쪽이 플러스(+), 아래쪽이 마이너스(-)입니다.

# 이 좌표를 알면 goto(x, y) 명령어로 거북이를 순간이동 시킬 수 있습니다.

# 거북이 조종법 (RC카와 똑같다!)

# 전진/후진: 거북이가 바라보는 방향을 기준으로 앞(forward)이나 뒤(backward)로 갑니다. (화면 위쪽이 무조건 앞이 아닙니다!)

# 회전: 제자리에서 왼쪽(left)이나 오른쪽(right)으로 돕니다. 이때 **각도(Degree)**가 중요합니다.

# 90도: 직각 (네모 그릴 때)

# 360도: 한 바퀴 (원 그릴 때)

# 펜 조절:

# up(): 펜 들기. 종이에서 펜을 떼니까 이동해도 선이 안 그려집니다.

# down(): 펜 내리기. 다시 그림을 그립니다.
# ==========================================
# 거북이 조종 핵심 3총사
# ==========================================
import turtle as t

# 0. 거북이 세팅 (모양, 속도, 굵기)
t.shape("turtle")
t.speed(3)       # 1(느림) ~ 10(빠름), 0(최고속도)
t.width(3)       # 선 굵기

# ------------------------------------------
# (1) 이동과 회전 (Forward & Turn)
# ------------------------------------------
t.color("blue")
print("1. 파란색 사각형을 그립니다.")

for i in range(4):
    t.forward(100) # 머리 방향으로 직진 (거리)
    t.left(90)     # 왼쪽으로 제자리 회전 (각도)

# ------------------------------------------
# (2) 펜 조절 (Up & Down) - 순간이동
# ------------------------------------------
print("2. 펜을 들고 옆으로 이동합니다.")

t.up()           # 펜 들기 (종이에서 떼기)
t.forward(200)   # 이동 (선이 안 그려짐!)
t.down()         # 펜 내리기 (다시 그릴 준비)

# ------------------------------------------
# (3) 좌표로 이동 (Goto)
# ------------------------------------------
t.color("red")
print("3. 빨간색 삼각형을 그립니다.")

# 삼각형 그리기
for i in range(3):
    t.forward(100)
    t.left(120)  # 360도 ÷ 3 = 120도

# (0, 0) 원점으로 돌아가기
t.up()
t.home() # 집(0,0)으로 가면서 머리 방향도 초기화

t.mainloop() # 창이 바로 꺼지지 않게 유지

# 왜 배우나요? (자동화)

# 매일 아침 학교에 오거나 집에 가서 컴퓨터를 켜면 하는 루틴이 있죠?

# 크롬 켜기 -> 2. 유튜브 들어가기 -> 3. 클래스룸 확인하기 -> 4. 웹툰 보기...

# 매일 똑같은 클릭을 10번씩 하는 것, 너무 귀찮지 않나요?

# 파이썬을 이용해 이 과정을 단 1초, 클릭 한 번으로 끝내는 것이 바로 **자동화(Automation)**입니다.

# 인터넷 조종 리모컨: webbrowser

# 파이썬이 직접 인터넷 창을 만드는 게 아닙니다.

# 파이썬이 윈도우(운영체제)에게 "야, 크롬 좀 켜서 이 주소로 들어가!" 라고 명령을 전달하는 방식입니다.

# 핵심 명령어: webbrowser.open("주소") - 이거 하나면 끝입니다.

# URL의 비밀 (검색어 자동화)

# 우리가 네이버나 구글 주소는 알지만, "검색 결과 페이지"로 바로 가는 법은 잘 모릅니다. 주소창을 자세히 보면 규칙이 있습니다.

# 구글 검색 주소: https://www.google.com/search?q=검색어

# 유튜브 검색 주소: https://www.youtube.com/results?search_query=검색어

# 이 규칙(?q=)을 이용하면, 파이썬에게 **"사용자한테 검색어를 입력받아서 주소 뒤에 붙여!"**라고 시킬 수 있습니다.
# ==========================================
# 인터넷 자동화 (웹 비서)
# ==========================================
import webbrowser
import time

print("🤖: 안녕하세요! 당신의 인터넷 비서입니다.")

# ------------------------------------------
# (1) URL 규칙 활용
# ------------------------------------------
keyword = "소마고"
# 구글 검색 주소의 규칙: search?q=검색어
# f-string을 이용해 주소를 완성합니다.
url = f"https://www.google.com/search?q={keyword}"

print(f"'{keyword}' 구글 검색을 띄웁니다...")
webbrowser.open(url) # 브라우저 실행!
